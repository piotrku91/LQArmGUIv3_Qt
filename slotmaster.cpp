QStringList* SlotMaster::GetActualIDs()
{
    ActualIDs.clear();
    for (auto OneSlot : Slot) {
    ActualIDs.append(OneSlot.IdName);
    };
    ActualIDs.append("BRAK");
  return &ActualIDs;
};
